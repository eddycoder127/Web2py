# -*- coding: utf-8 -*-
db.define_table('Live_Updates',
                Field('title','string',requires=IS_NOT_EMPTY()),
                Field('body','text',requires=IS_NOT_EMPTY()),
                auth.signature)
db.define_table('News',
                Field('category','string'),
                Field('short_title','text'),
                Field('image','upload'),
                Field('news','text'))
db.define_table('News_comment',
                Field('title', db.News),
                Field('body','text',requires=IS_NOT_EMPTY()),
                auth.signature)
